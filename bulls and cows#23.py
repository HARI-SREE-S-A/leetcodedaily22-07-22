    "class Solution:\n",
    "    def getHint(self, secret: str, guess: str) -> str:\n",
    "        \n",
    "        # Setup counts for bulls and cows\n",
    "        bulls = cows = 0\n",
    "        \n",
    "        # Copy secret and guess into lists that are easier to work with\n",
    "        secretCopy = list(secret)\n",
    "        guessCopy = list(guess)\n",
    "        \n",
    "        # In a for loop, check every pair of letters at the same index in both guess and secret for matching letters, AKA bulls\n",
    "        for i in range(len(secret)):\n",
    "            \n",
    "            # If they match, bulls += 1 and pop() the letters from the copy lists via their .index()\n",
    "            if secret[i] == guess[i]:\n",
    "                bulls += 1\n",
    "                secretCopy.pop(secretCopy.index(secret[i]))\n",
    "                guessCopy.pop(guessCopy.index(guess[i]))\n",
    "                \n",
    "                \n",
    "        # Count() the letters remaining in secret and guess lists\n",
    "        secretCounter = Counter(secretCopy)\n",
    "        guessCounter = Counter(guessCopy)\n",
    "        \n",
    "        # Counter1 - Counter2 gives us Counter1 with any matching values of Counter1 and Counter2 removed; leftover Counter2 values are trashed\n",
    "        # secretCounter - guessCounter gives us the secretCounter except for any correctly guessed letters\n",
    "        # Therefore, subtract this difference from the OG secretCounter to be left with a counter of only correctly guessed letters\n",
    "        dif = secretCounter - (secretCounter - guessCounter)\n",
    "        \n",
    "        # The .total() of the dif Counter is the number of cows\n",
    "        cows = dif.total()\n",
    "\n",
    "        # return the formatted string with req. info\n",
    "        return f'{bulls}A{cows}B'"
